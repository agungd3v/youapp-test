import { toast } from "react-toastify";

export const toastSuccess = (param: string) => {
  return toast.success(param, {
    position: "bottom-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "dark",
  });
}

